package com.chenxianyu.newes.Pojo;

public class Tag {
    private int tagId; // 标签ID
    private String name;
    // 标签名称

    public int getTagId() {
        return tagId;
    }

    public void setTagId(int tagId) {
        this.tagId = tagId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
